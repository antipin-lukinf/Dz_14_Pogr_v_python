import doctest
from string import ascii_lowercase


def removal_chars(text: str) -> str:
    """
    >>> removal_chars('FFFggggY5') == 'fffggggy'
    True
    >>> removal_chars('sss   hhHH7&8') ==  'ssshhhh'
    True
    >>> removal_chars('5 6 9 8 7  )))))   ') == '          '
    False
    """
    result = ''
    for ch in text.lower():
        if ch in ascii_lowercase + '':
            result += ch

    return result


